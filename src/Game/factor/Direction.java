package Game.factor;

/**
 * Diese Klasse representiert alle möglichen richtungen in die man von einem {@link Game.object.Room} über die {@link Game.object.Room#neighborRooms}gehen kann.
 */
public enum Direction
{
    NORTH,
    EAST,
    SOUTH1,
    SOUTH2,
    SOUTH3,
    WEST1,
    WEST2,
    RESET,
}
